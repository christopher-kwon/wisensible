package com.hope.db;

public class HopeBean {
	private int hope_num;
	private int board_num;
	private String hope_id;
	private int hope_price;
	private String hope_date;
	
	public int getHope_num() {
		return hope_num;
	}
	public void setHope_num(int hope_num) {
		this.hope_num = hope_num;
	}

	public String getHope_id() {
		return hope_id;
	}
	public void setHope_id(String hope_id) {
		this.hope_id = hope_id;
	}
	public int getHope_price() {
		return hope_price;
	}
	public void setHope_price(int hope_price) {
		this.hope_price = hope_price;
	}
	public String getHope_date() {
		return hope_date;
	}
	public void setHope_date(String hope_date) {
		this.hope_date = hope_date;
	}
	public int getBoard_num() {
		return board_num;
	}
	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}
	
}
	
	